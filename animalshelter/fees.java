package animalshelter;

public class fees{
    private int adoptionFees;
    private int vaccinationFees;
    private int microchipFees;

    public int getAdoptionFees(){
        return adoptionFees;
    }

    public void setAdoptionFees(int adoptionFees){
        this.adoptionFees = adoptionFees;
    }
  
    public int getVaccinationFees(){
        return vaccinationFees;
    }

    public void setVaccinationFees(int vaccinationFees){
        this.vaccinationFees = vaccinationFees;
    }

    public int getMicrochipFees(){
        return microchipFees;
    }

    public void setMicrochipFees(int microchipFees){
        this.microchipFees = microchipFees;
    }

}